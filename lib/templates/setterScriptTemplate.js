/**
 * Fill template with business-specific values
 */
export function fillSetterScriptTemplate(mode, placeholders) {
  const template = mode === 'Service Delivery'
    ? SETTER_SCRIPT_TEMPLATE.serviceDelivery
    : SETTER_SCRIPT_TEMPLATE.transformation;

  let filledScript = template;

  // Replace all placeholders
  Object.entries(placeholders).forEach(([key, value]) => {
    const placeholder = `{${key}}`;
    filledScript = filledScript.replace(new RegExp(placeholder, 'g'), value || '');
  });

  return filledScript;
}

export default { SETTER_SCRIPT_TEMPLATE, fillSetterScriptTemplate };
